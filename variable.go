package main

import "fmt"

func main() {

	var name string

	name = "Eko Kurniawan"
	fmt.Println(name)

	name = "Eko Khannedy"
	fmt.Println(name)

	var friendName = "Eko Kurniawan"
	fmt.Println(friendName)

	country := "Indonesia"
	fmt.Println(country)

	country = "America"
	fmt.Println(country)

	var (
		firstName = "Eko"
		lastName  = "Khannedy"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)

}
